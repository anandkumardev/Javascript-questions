// === Program to Convert Celsius to Fahrenheit === //

// "Formula" ::: fahrenheit = celsius * 1.8 + 32


const celsius = 55;

// calculate fahrenheit
const fahrenheit = (celsius * 1.8) + 32

// display the result
console.log(`${celsius} degree celsius is equal to ${fahrenheit} degree fahrenheit.`); // 55 degree celsius is equal to 131 degree fahrenheit.



